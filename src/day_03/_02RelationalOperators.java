package day_03;

public class _02RelationalOperators {

    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 5;

        boolean res;

        // == verwendet man um 2 werte zu vergleichen ob beide gleich ist
        res = num1 == num2; //true

        System.out.println(num1 == num2);

        System.out.println(res);

        res = num1 != num2;
        System.out.println(res);

        res = num1 < num2;

        System.out.println(res);

        res = num1 > num2;

        System.out.println(res);

        res = num1 >= num2;

        System.out.println(res);

        res = num1 <= num2;

        System.out.println(res);








    }

}
