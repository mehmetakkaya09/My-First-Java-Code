package day_07;

public class _04ForLoops4 {

    public static void main(String[] args) {

        for (double i = 1.5; i <= 5.5; i++) {
            System.out.println("Eurotech : " + i);
        }

        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.println(i);
        }

        for (int i = 0; i < 5 && i > -5; i++) {
            System.out.println(i);
        }

        for (int i = 0; i < 5;) {
            i++;
            System.out.println("Eurotech : " + i);
        }

//        for (;;){
//            System.out.println("Eurotech : ");
//        }









    }
}
